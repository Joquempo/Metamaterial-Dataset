#!/bin/bash
eval "$(conda shell.bash hook)"
conda update -n base -c defaults conda -y
conda create -n metamaterial -y python=3.8
conda activate metamaterial
conda config --add channels conda-forge
conda config --set channel_priority strict

echo " "
echo "____________________________ ____________________________ ____________________________"
echo "======= NUMPY ============== <><><><><><><><><><><><><><> <><><><><><><><><><><><><><>"
conda install numpy -y
echo " "
echo "____________________________ ____________________________ ____________________________"
echo "======= SCIPY ============== <><><><><><><><><><><><><><> <><><><><><><><><><><><><><>"
conda install scipy -y
echo " "
echo "____________________________ ____________________________ ____________________________"
echo "======= MATPLOTLIB ========= <><><><><><><><><><><><><><> <><><><><><><><><><><><><><>"
conda install matplotlib -y
echo " "
echo "____________________________ ____________________________ ____________________________"
echo "======= CYTHON ============= <><><><><><><><><><><><><><> <><><><><><><><><><><><><><>"
conda install cython -y
echo " "
echo "____________________________ ____________________________ ____________________________"
echo "======= SCIKIT-SPARSE ====== <><><><><><><><><><><><><><> <><><><><><><><><><><><><><>"
conda install scikit-sparse -y
echo " "
echo "____________________________ ____________________________ ____________________________"
echo "======= PULP =============== <><><><><><><><><><><><><><> <><><><><><><><><><><><><><>"
conda install pulp -y
echo " "
echo "____________________________ ____________________________ ____________________________"
echo "____________________________ ____________________________ ____________________________"
echo "____________________________ ____________________________ ____________________________"
cd ./cython
python ./cython_setup.py build_ext --inplace
