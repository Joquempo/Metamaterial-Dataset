import numpy as np

def getJ(xi,eta,sv):
    dN = 0.25*np.array([[-(1-eta), (1-eta), (1+eta), -(1+eta)],
                        [ -(1-xi), -(1+xi),  (1+xi),   (1-xi)]])
    J = dN @ sv
    Jdet = J[0,0]*J[1,1] - J[0,1]*J[1,0]
    Jinv = (1/Jdet) * np.array([[ J[1,1], -J[0,1]],
                                [-J[1,0],  J[0,0]]])
    return Jinv, Jdet

def getB(xi,eta,sv):
    Jinv, Jdet = getJ(xi,eta,sv)
    Ai = np.array([[1,0,0,0],
                   [0,0,0,1],
                   [0,1,1,0]])
    Aj = np.zeros((4,4))
    Aj[0:2,0:2] = Jinv
    Aj[2:4,2:4] = Jinv
    Ak = 0.25*np.array([[-(1-eta), 0.0, (1-eta), 0.0, (1+eta), 0.0, -(1+eta), 0.0],
                        [ -(1-xi), 0.0, -(1+xi), 0.0,  (1+xi), 0.0,   (1-xi), 0.0],
                        [0.0, -(1-eta), 0.0, (1-eta), 0.0, (1+eta), 0.0, -(1+eta)],
                        [0.0,  -(1-xi), 0.0, -(1+xi), 0.0,  (1+xi), 0.0,   (1-xi)]])
    Bmat = Ai @ Aj @ Ak
    return Bmat, Jdet

def get_emat(Ey,nu):
    # constitutive matrix
    Ce = (Ey/(1-nu**2))*np.array([[1,nu,0],[nu,1,0],[0,0,(1-nu)/2]])
    # elements nodes
    sv = np.ndarray((12,4,2))
    s3 = np.sqrt(3)
    sv[0,:,:]  = np.array([[-0.5,  0.5*s3],
                           [ 0.0,  0.0   ],
                           [ 1.0,  0.0   ],
                           [ 1.0,      s3]])
    sv[1,:,:]  = np.array([[-1.0,  0.0   ],
                           [ 0.0,  0.0   ],
                           [ 0.5,  0.5*s3],
                           [-1.0,      s3]])
    sv[2,:,:]  = np.array([[-0.5, -0.5*s3],
                           [ 0.0,  0.0   ],
                           [-0.5,  0.5*s3],
                           [-2.0,  0.0   ]])
    sv[3,:,:]  = np.array([[ 0.5, -0.5*s3],
                           [ 0.0,  0.0   ],
                           [-1.0,  0.0   ],
                           [-1.0,     -s3]])
    sv[4,:,:]  = np.array([[ 1.0,  0.0   ],
                           [ 0.0,  0.0   ],
                           [-0.5, -0.5*s3],
                           [ 1.0,     -s3]])
    sv[5,:,:]  = np.array([[ 0.5,  0.5*s3],
                           [ 0.0,  0.0   ],
                           [ 0.5, -0.5*s3],
                           [ 2.0,  0.0   ]])    
    sv[6,:,:]  = np.array([[ 0.0,  0.0   ],
                           [ 0.5, -0.5*s3],
                           [ 2.0,  0.0   ],
                           [ 1.5,  0.5*s3]])
    sv[7,:,:]  = np.array([[ 0.0,  0.0   ],
                           [ 1.0,  0.0   ],
                           [ 1.0,      s3],
                           [ 0.0,      s3]])
    sv[8,:,:]  = np.array([[ 0.0,  0.0   ],
                           [ 0.5,  0.5*s3],
                           [-1.0,      s3],
                           [-1.5,  0.5*s3]])
    sv[9,:,:]  = np.array([[ 0.0,  0.0   ],
                           [-0.5,  0.5*s3],
                           [-2.0,  0.0   ],
                           [-1.5, -0.5*s3]])
    sv[10,:,:] = np.array([[ 0.0,  0.0   ],
                           [-1.0,  0.0   ],
                           [-1.0,     -s3],
                           [ 0.0,     -s3]])
    sv[11,:,:] = np.array([[ 0.0,  0.0   ],
                           [-0.5, -0.5*s3],
                           [ 1.0,     -s3],
                           [ 1.5, -0.5*s3]])
    # stiffness matrices
    Ket = np.ndarray((12,8,8))
    gpoint = s3/3
    for ek in range(12):
        B0, J0 = getB(-gpoint, -gpoint, sv[ek,:,:])
        B1, J1 = getB( gpoint, -gpoint, sv[ek,:,:])
        B2, J2 = getB( gpoint,  gpoint, sv[ek,:,:])
        B3, J3 = getB(-gpoint,  gpoint, sv[ek,:,:])
        Ket[ek,:,:] = J0 * B0.T @ Ce @ B0 + J1 * B1.T @ Ce @ B1 + J2 * B2.T @ Ce @ B2 + J3 * B3.T @ Ce @ B3
        Ket[ek,:,:] = 0.5*(Ket[ek,:,:]+Ket[ek,:,:].T)
    return Ket

def get_augmat(Ns,inci,etype,sym,dKe,small=1e-14):
    N = Ns**2  # number of elements in the design domain
    # different unconstrained augmented elements
    aug_etype = np.ndarray((N),dtype=np.uint8)
    aug_etype[0:N-1:Ns+1]                   = 0
    for k in range(Ns-2):
        aug_etype[k*(Ns+1)+1:(k+1)*Ns-1]    = 1
        aug_etype[(k+1)*(Ns+1)-1:N-Ns-1:Ns] = 2
    aug_etype[Ns-1:N-1:Ns]                  = 3
    aug_etype[N-Ns:N-1]                     = 4
    aug_etype[N-1]                          = 5
    # factorization
    Hlist = []
    dKelist = []
    eledofs = np.ndarray((6,8),dtype=np.uint32)
    locdofs = np.ndarray((6,8),dtype=np.uint32)
    for e in [0,1,Ns,Ns-1,N-Ns,N-1]:
        for k in range(6):
            et = sym[e,k]
            n0 = inci[et,0]
            n1 = inci[et,1]
            n2 = inci[et,2]
            n3 = inci[et,3]
            eledofs[k,:] = np.array([2*n0,2*n0+1,2*n1,2*n1+1,2*n2,2*n2+1,2*n3,2*n3+1])
        size = len(np.unique(eledofs))
        elebool = np.ones((6,8),dtype=bool)
        kkk = 0
        for k in range(6):
            for kk in range(8):
                if elebool[k,kk]:
                    mask = (eledofs==eledofs[k,kk])
                    locdofs[mask] = kkk
                    elebool[mask] = False
                    kkk = kkk + 1
        aug_dKe = np.zeros((size,size))
        for k in range(6):
            et = sym[e,k]
            ek = etype[et]
            grid = np.ix_(locdofs[k,:],locdofs[k,:])
            aug_dKe[grid] = aug_dKe[grid] + dKe[ek,:,:]
        D,V = np.linalg.eigh(aug_dKe)
        mask = abs(D) > small
        D = D[mask]
        V = V[:,mask]
        He = V*np.sqrt(D)
        Hlist = Hlist + [He]
        dKelist = dKelist + [aug_dKe]
    return aug_etype, Hlist, dKelist