#%% Imports
import os, sys, shutil

noptf = 7  # number of optimizations stored in the same file

#%% Dataset Verification

if not os.path.exists('./SILP/output'):
    print('no output directory')
    sys.exit()
runs = sorted(os.listdir('./SILP/output'))
if len(runs) == 0:
    print('no runs have been found')
    sys.exit()
total = 0
r2 = -1
for k in range(len(runs)):
    run_dir = './SILP/output/' + runs[k] + '/'
    r1 = int(runs[k][4:9])
    if r1 <= r2:
        print('redundant runs : ' + runs[k])
        sys.exit()
    r2 = int(runs[k][10:])
    rnum = r2 - r1 + 1
    files = sorted(os.listdir(run_dir))
    if files[-1] == 'logs':
        files = files[:-1]
    else:
        print('missing logs directory : ' + runs[k])
        sys.exit()
    if rnum % noptf != 0:
        print('number of cases is not a multiple of noptf : ' + runs[k])
        sys.exit()
    if noptf*len(files) != rnum:
        print('incoherent number of files : ' + runs[k])
        sys.exit()
    total += rnum
    for kk in range(len(files)):
        file_dir = run_dir + files[kk]
        if len(os.listdir(file_dir)) != 27:
            print('wrong number of files : ' + runs[k] + '/' + files[kk])
            sys.exit()
if total % noptf != 0:
    print('something is wrong...')
    sys.exit()
total = total // noptf
print('valid SILP dataset!')
print('{:04d} / {:04d} files in the SILP dataset ({:5.1f} %)\n'.format(
      total,18382//noptf,total*noptf*100/18382))

# check directories
if not os.path.exists('../../dataset'):
    os.mkdir('../../dataset')
if not os.path.exists('../../dataset/SILP'):
    os.mkdir('../../dataset/SILP')
else:
    if len(os.listdir('../../dataset/SILP')) > 0:
        print('a SILP dataset has already been generated, rename its directory before generating a new one')
        sys.exit()

#%% Dataset Generation

global_id = 0
runs = sorted(os.listdir('./SILP/output'))
for k in range(len(runs)):
    run_dir = './SILP/output/' + runs[k] + '/'
    files = sorted(os.listdir(run_dir))
    files = files[:-1]
    for kk in range(len(files)):
        if global_id % 100 == 0:
            print(': {:04d} / {:04d} : files have been moved to the SILP dataset ({:5.1f} %)'.format(
                  global_id,total,global_id*100/total))
        file_dir = run_dir + files[kk] + '/'
        destination = '../../dataset/SILP/f{:04d}'.format(global_id)
        os.mkdir(destination)
        for file in os.listdir(file_dir):
            source = file_dir + file
            shutil.move(source, destination)
        global_id += 1
print(': {:04d} / {:04d} : files have been moved to the SILP dataset ({:5.1f} %)'.format(
      global_id,total,global_id*100/total))
print('[ SILP dataset generated ]')
