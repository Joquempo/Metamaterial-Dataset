import os
import numpy as np

# check directories
if not os.path.exists('./input'):
    os.mkdir('./input')

Ey = 1.00  # Young's modulus of the base material
nu = 0.30  # Poisson's ratio of the base material

nuval_list = np.concatenate((np.linspace(-1.0,nu-0.1,1+int(100*(nu+0.9))),np.linspace(nu+0.1,1.0,1+int(100*(0.9-nu)))))
nu_num = len(nuval_list)
Eymin_list = np.linspace(0,0.5*Ey,101)
Ey_num = len(Eymin_list)

print('generating input files')
total_num = nu_num*Ey_num
inputmat = np.ndarray([total_num,2],dtype=np.float32)
fid = 0
for knu in range(nu_num):
    print(': {:05d} / {:05d} :'.format(knu*Ey_num+1,total_num))
    for kEy in range(Ey_num):
        input_num = knu*Ey_num + kEy
        nuval = np.float32(nuval_list[knu])  # target Poisson's ratio
        Eymin = np.float32(Eymin_list[kEy])  # minimal Young's modulus
        inputmat[fid,0] = nuval
        inputmat[fid,1] = Eymin
        fid = fid + 1
print(': {:05d} / {:05d} :'.format(input_num+1,total_num))
np.save('./input/inputmat.npy',inputmat)
print('[ input file generated ]')
print('done!')